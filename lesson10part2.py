class OperasiMatematika:
    @staticmethod
    def penjumlahan(bil1,bil2):
        hasil = bil1 + bil2
        print('Hasil penjumlahan',bil1,'dan',bil2,'adalah',hasil)

    @staticmethod
    def pengurangan(bil1,bil2):
        hasil = bil1 - bil2
        print('Hasil pengurangan', bil1, 'dan', bil2, 'adalah', hasil)

    @staticmethod
    def perkalian(bil1,bil2):
        hasil = bil1 * bil2
        print('Hasil perkalian', bil1, 'dan', bil2, 'adalah', hasil)

    @staticmethod
    def pembagian(bil1,bil2):
        hasil = bil1 / bil2
        print('Hasil pembagian', bil1, 'dan', bil2, 'adalah', hasil)